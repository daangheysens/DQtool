	public boolean isValidLocalLov(Affiliate aff, String type, String lov)
	{
		StringBuilder key = new StringBuilder();		
		key.append(aff.getAffiliateName().toLowerCase());
		key.append(type);
		String cleanedKey = key.toString().trim().toLowerCase();

		if (!localLovs.get(cleanedKey).contains(lov))
		{
			return false;
		}

		return true;
	}

	public boolean isValidGlobalLov(String type, String lov)
	{
		if (globalLovs.get(type.toLowerCase().trim()).contains(lov))
		{
			return true;
		}
		return false;
	}
	
